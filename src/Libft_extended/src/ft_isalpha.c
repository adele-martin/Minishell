int	ft_isalpha(int c)
{
	return ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'));
}

int	isalnum_space(int c)
{
    return (ft_isalpha(c) || ft_isdigit(c) || c == 32);
}